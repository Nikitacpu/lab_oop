# Определение класса Quadrilateral, представляющего четырехугольник
class Quadrilateral:
    # Конструктор класса, принимает один или два аргумента
    def __init__(self, width, height=None):
        # Инициализация ширины четырехугольника
        self.width = width
        # Инициализация высоты четырехугольника, если она указана, иначе делаем его квадратом
        self.height = height if height is not None else width
    # Переопределение метода __str__ для красивого вывода информации о четырехугольнике
    def __str__(self):
        # Если ширина и высота одинаковые, то это куб, иначе - прямоугольник
        if self.width == self.height:
            return f"Куб размером {self.width}х{self.height}"
        else:
            return f"Прямоугольник размером {self.width}х{self.height}"
    # Переопределение метода __bool__ для проверки, является ли четырехугольник квадратом
    def __bool__(self):
        return self.width == self.height
# Создание экземпляра класса Quadrilateral с одним аргументом, что делает его квадратом
q1 = Quadrilateral(10)
# Вывод информации о квадрате
print(q1)  # печатает "Куб размером 10х10"
# Проверка, является ли квадрат квадратом (выводит True)
print(bool(q1))  # печатает "True"
# Создание экземпляра класса Quadrilateral с двумя аргументами, что делает его прямоугольником
q2 = Quadrilateral(3, 5)
# Вывод информации о прямоугольнике
print(q2)  # печатает "Прямоугольник размером 3х5"
# Проверка, является ли прямоугольник квадратом (выводит False)
print(bool(q2))  # печатает "False"
